## 2 functions are defined here to calculate and cache the inverse of a matrix
## 1. makeCacheMatrix
## 2. cacheSolve

## makeCacheMatrix function creates a matrix and caches it's inverse

makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setinv <- function(solve) m <<- solve
  getinv <- function() m
  list(set = set, get = get,
       setinv = setinv,
       getinv = getinv)
  
}


## cacheSolve tries to get the pre-cached inverse of the given matrix. 
## If couldn't find it, then calculates and send it to cache and console

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
    m <- x$getinv()
    if(!is.null(m)) {
      message("getting cached data")
      return(m)
    }
    data <- x$get()
    m <- solve(data, ...)
    x$setinv(m)
    m
  }
